class Operation:
    def add(num1, num2):
        total=num1+num2
        return total

    def difference(num1, num2):
        minus=num1-num2
        return minus

    def product(num1, num2):
        prod=num1*num2
        return prod

    def division(num1, num2):
        quotient=num1/num2
        return quotient
